package com.BlackStar.BlackStarVO;

public class BlackStarBookVO {

	private String book_ondate;
	private String book_outdate;
	private String book_name;
	private String book_content;
	
	public String getBook_ondate() {
		return book_ondate;
	}
	public void setBook_ondate(String book_ondate) {
		this.book_ondate = book_ondate;
	}
	public String getBook_outdate() {
		return book_outdate;
	}
	public void setBook_outdate(String book_outdate) {
		this.book_outdate = book_outdate;
	}
	public String getBook_name() {
		return book_name;
	}
	public void setBook_name(String book_name) {
		this.book_name = book_name;
	}
	public String getBook_content() {
		return book_content;
	}
	public void setBook_content(String book_content) {
		this.book_content = book_content;
	}
	
	
}