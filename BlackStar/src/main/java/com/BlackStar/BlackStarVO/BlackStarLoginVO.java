package com.BlackStar.BlackStarVO;

import java.io.Serializable;

public class BlackStarLoginVO implements Serializable{

	// 클라이언트에서 쓰이는 폼의 name값을 지정
	private String loginid;
	private String loginsecret;

	public BlackStarLoginVO() {

	}

	public BlackStarLoginVO(String loginid, String loginsecret) {
		this.loginid = loginid;
		this.loginsecret = loginsecret;
	}

	public String getLoginid() {
		return loginid;
	}

	public void setLoginid(String loginid) {
		this.loginid = loginid;
	}

	public String getLoginsecret() {
		return loginsecret;
	}

	public void setLoginsecret(String loginsecret) {
		this.loginsecret = loginsecret;
	}

}
